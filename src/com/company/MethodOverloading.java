package com.company;

public class MethodOverloading {
    public static void printn(int x){
        System.out.println(x);
    }
    public static  void printn(double x){
        System.out.println(x);
    }
    public static void printn(long x){
        System.out.println(x);
    }
    public static void printn(float x){
        System.out.println(x);
    }

}
