package com.company;

public class arthematicop {

    public static void main(String[] args) {
       int num1 = 45;
       int num2 = 56;


        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        System.out.println(num1++);
        System.out.println(--num1);

    }
}


